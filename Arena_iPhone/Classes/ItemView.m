#import "ItemView.h"


@implementation ItemView

-(id) initWithFrame:(CGRect)frame
{
	id ret = [super initWithFrame:frame];
	return ret;
}

-(void) setPosition:(int) x: (int) y
{
	CGRect newFrame = [self frame];
	newFrame.origin.x = x;
	newFrame.origin.y = y;
	[self setFrame:newFrame];
}
@end
